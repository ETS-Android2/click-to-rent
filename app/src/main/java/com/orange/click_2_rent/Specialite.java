package com.orange.click_2_rent;

public class Specialite {

    int imageDesc;
    String nomDesc;

    public Specialite(int imageDesc, String nomDesc) {

        this.imageDesc = imageDesc;
        this.nomDesc = nomDesc;
    }

    public int getImageDesc() {

        return imageDesc;
    }

    public String getNomDesc() {

        return nomDesc;
    }
}
